package idv.hic.android.lazycontacts.model;

public class ContactGroup {

	
	private int id;
	private String title;
	private int summaryCount;
	private int summaryHasPhoneCount;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public int getSummaryCount() {
		return summaryCount;
	}
	public void setSummaryCount(int summaryCount) {
		this.summaryCount = summaryCount;
	}
	public int getSummaryHasPhoneCount() {
		return summaryHasPhoneCount;
	}
	public void setSummaryHasPhoneCount(int summaryHasPhoneCount) {
		this.summaryHasPhoneCount = summaryHasPhoneCount;
	}
	
	
	
}
